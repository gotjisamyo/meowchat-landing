// Typing indicator dots
function TypingIndicator() {
  return (
    <div className="flex items-end gap-1 px-4 py-3">
      {[0, 1, 2].map((i) => (
        <span
          key={i}
          className="w-2 h-2 rounded-full bg-purple-400 inline-block"
          style={{
            animation: "bounce 1.2s ease-in-out infinite",
            animationDelay: `${i * 0.2}s`,
          }}
        />
      ))}
      <style>{`
        @keyframes bounce {
          0%, 60%, 100% { transform: translateY(0); opacity: 0.5; }
          30% { transform: translateY(-6px); opacity: 1; }
        }
      `}</style>
    </div>
  );
}

let msgId = 0;

export default function DemoPlayground() {
  const [business, setBusiness] = useState<BusinessType>("clinic");
  const [personality, setPersonality] = useState<Personality>("friendly");
  const [language, setLanguage] = useState<Language>("th");
  const [botName, setBotName] = useState("MeowBot");
  const [messages, setMessages] = useState<Message[]>([]);
  const [input, setInput] = useState("");
  const [isTyping, setIsTyping] = useState(false);
  const chatEndRef = useRef<HTMLDivElement>(null);

  // Send initial greeting when business or personality changes
  useEffect(() => {
    const greeting = getGreeting(business, personality, botName || "MeowBot");
    setMessages([
      {
        id: ++msgId,
        role: "bot",
        text: greeting,
        ts: Date.now(),
      },
    ]);
  }, [business, personality, botName]);

  // Auto-scroll to bottom
  useEffect(() => {
    chatEndRef.current?.scrollIntoView({ behavior: "smooth" });
  }, [messages, isTyping]);

  const sendMessage = (text: string) => {
    if (!text.trim() || isTyping) return;
    const userMsg: Message = { id: ++msgId, role: "user", text: text.trim(), ts: Date.now() };
    setMessages((prev) => [...prev, userMsg]);
    setInput("");
    setIsTyping(true);
    const delay = 800 + Math.random() * 700;
    setTimeout(() => {
      const response = getBotResponse(text, business, personality, botName || "MeowBot");
      setMessages((prev) => [
        ...prev,
        { id: ++msgId, role: "bot", text: response, ts: Date.now() },
      ]);
      setIsTyping(false);
    }, delay);
  };

  const handleReset = () => {
    const greeting = getGreeting(business, personality, botName || "MeowBot");
    setMessages([{ id: ++msgId, role: "bot", text: greeting, ts: Date.now() }]);
    setInput("");
    setIsTyping(false);
  };

  const currentBusiness = BUSINESS_OPTIONS.find((b) => b.id === business)!;

  return (
    <div className="glass rounded-3xl p-6 md:p-8 border-purple-500/20">
      <div className="flex flex-col lg:flex-row gap-6 lg:gap-8">
        {/* Left: Config panel */}
        <div className="lg:w-72 flex-shrink-0 space-y-5">
          {/* Business type */}
          <div>
            <p className="text-xs font-semibold text-gray-400 uppercase tracking-wider mb-3">
              ประเภทธุรกิจ
            </p>
            <div className="grid grid-cols-2 gap-2">
              {BUSINESS_OPTIONS.map((b) => (
                <button
                  key={b.id}
                  onClick={() => setBusiness(b.id)}
                  className={`flex items-center gap-1.5 px-2.5 py-2 rounded-xl text-xs font-medium transition-all ${
                    business === b.id
                      ? "bg-purple-600/40 border border-purple-500/60 text-white"
                      : "bg-white/5 border border-white/10 text-gray-400 hover:bg-white/10 hover:text-gray-200"
                  }`}
                >
                  <span className="text-base leading-none">{b.icon}</span>
                  <span className="leading-tight">{b.label}</span>
                </button>
              ))}
            </div>
          </div>

          {/* Personality */}
          <div>
            <p className="text-xs font-semibold text-gray-400 uppercase tracking-wider mb-3">
              บุคลิก Bot
            </p>
            <div className="grid grid-cols-2 gap-2">
              {PERSONALITY_OPTIONS.map((p) => (
                <button
                  key={p.id}
                  onClick={() => setPersonality(p.id)}
                  className={`flex flex-col items-start px-3 py-2.5 rounded-xl text-xs font-medium transition-all ${
                    personality === p.id
                      ? "bg-purple-600/40 border border-purple-500/60 text-white"
                      : "bg-white/5 border border-white/10 text-gray-400 hover:bg-white/10 hover:text-gray-200"
                  }`}
                >
                  <span className="text-base mb-0.5">{p.icon}</span>
                  <span className="font-semibold">{p.label}</span>
                  <span className="text-[10px] opacity-70">{p.desc}</span>
                </button>
              ))}
            </div>
          </div>

          {/* Bot name */}
          <div>
            <p className="text-xs font-semibold text-gray-400 uppercase tracking-wider mb-2">
              ชื่อ Bot
            </p>
            <input
              type="text"
              value={botName}
              onChange={(e) => setBotName(e.target.value)}
              placeholder="MeowBot"
              maxLength={20}
              className="w-full bg-white/5 border border-white/10 rounded-xl px-3 py-2 text-sm text-white placeholder-gray-500 focus:outline-none focus:border-purple-500/60 transition-colors"
            />
          </div>

          {/* Language toggle */}
          <div>
            <p className="text-xs font-semibold text-gray-400 uppercase tracking-wider mb-2">
              ภาษา
            </p>
            <div className="flex gap-2">
              {(["th", "en", "both"] as Language[]).map((l) => (
                <button
                  key={l}
                  onClick={() => setLanguage(l)}
                  className={`flex-1 py-1.5 rounded-lg text-xs font-medium transition-all ${
                    language === l
                      ? "bg-purple-600/40 border border-purple-500/60 text-white"
                      : "bg-white/5 border border-white/10 text-gray-400 hover:bg-white/10"
                  }`}
                >
                  {l === "th" ? "ไทย" : l === "en" ? "English" : "ทั้งคู่"}
                </button>
              ))}
            </div>
          </div>
        </div>

        {/* Right: Chat window */}
        <div className="flex-1 flex flex-col min-w-0">
          {/* Chat header */}
          <div className="flex items-center justify-between mb-3">
            <div className="flex items-center gap-3">
              <div className="w-9 h-9 rounded-full bg-purple-600/30 border border-purple-500/40 flex items-center justify-center text-lg">
                {currentBusiness.icon}
              </div>
              <div>
                <p className="text-sm font-semibold text-white">{botName || "MeowBot"}</p>
                <p className="text-[11px] text-green-400 flex items-center gap-1">
                  <span className="w-1.5 h-1.5 rounded-full bg-green-400 inline-block" />
                  ออนไลน์อยู่
                </p>
              </div>
            </div>
            <button
              onClick={handleReset}
              className="text-xs text-gray-400 hover:text-gray-200 px-3 py-1.5 rounded-lg bg-white/5 border border-white/10 hover:bg-white/10 transition-all"
            >
              รีเซ็ต
            </button>
          </div>

          {/* Messages */}
          <div
            className="flex-1 overflow-y-auto space-y-3 pr-1 mb-3"
            style={{ height: "400px", maxHeight: "400px" }}
          >
            {messages.map((msg) => (
              <div
                key={msg.id}
                className={`flex ${msg.role === "user" ? "justify-end" : "justify-start"}`}
              >
                <div
                  className={`max-w-[80%] px-4 py-2.5 rounded-2xl text-sm leading-relaxed ${
                    msg.role === "user"
                      ? "bg-zinc-700 text-white rounded-br-sm"
                      : "bg-purple-600/20 border border-purple-500/30 text-gray-100 rounded-bl-sm"
                  }`}
                >
                  {msg.text}
                </div>
              </div>
            ))}
            {isTyping && (
              <div className="flex justify-start">
                <div className="bg-purple-600/20 border border-purple-500/30 rounded-2xl rounded-bl-sm">
                  <TypingIndicator />
                </div>
              </div>
            )}
            <div ref={chatEndRef} />
          </div>

          {/* Quick replies */}
          <div className="flex flex-wrap gap-2 mb-3">
            {QUICK_REPLIES[business].map((q) => (
              <button
                key={q}
                onClick={() => sendMessage(q)}
                disabled={isTyping}
                className="text-xs px-3 py-1.5 rounded-full bg-purple-600/15 border border-purple-500/25 text-purple-300 hover:bg-purple-600/25 hover:text-purple-200 transition-all disabled:opacity-40 disabled:cursor-not-allowed"
              >
                {q}
              </button>
            ))}
          </div>

          {/* Input */}
          <div className="flex gap-2">
            <input
              type="text"
              value={input}
              onChange={(e) => setInput(e.target.value)}
              onKeyDown={(e) => {
                if (e.key === "Enter" && !e.shiftKey) {
                  e.preventDefault();
                  sendMessage(input);
                }
              }}
              placeholder="พิมพ์ข้อความ..."
              disabled={isTyping}
              className="flex-1 bg-white/5 border border-white/10 rounded-xl px-4 py-2.5 text-sm text-white placeholder-gray-500 focus:outline-none focus:border-purple-500/60 transition-colors disabled:opacity-50"
            />
            <button
              onClick={() => sendMessage(input)}
              disabled={!input.trim() || isTyping}
              className="px-4 py-2.5 bg-purple-600 hover:bg-purple-700 disabled:bg-purple-600/30 disabled:cursor-not-allowed text-white rounded-xl text-sm font-semibold transition-all"
            >
              ส่ง
            </button>
          </div>
        </div>
      </div>
    </div>
  );
}
