function getGreeting(
  business: BusinessType,
  personality: Personality,
  botName: string,
): string {
  const variants = GREETINGS[business][personality];
  const base = variants[Math.floor(Math.random() * variants.length)];
  return base.replace(/MeowBot|ผม|บอท/g, (m) => {
    if (m === "ผม") return botName || "ผม";
    return botName || m;
  });
}

